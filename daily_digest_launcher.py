async def review_categorization_for_date(db_manager, date):
    """–ü—Ä–æ–≤–µ—Ä–∫–∞ –∏ –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∑–∞—Ü–∏–∏ —Å–æ–æ–±—â–µ–Ω–∏–π –∑–∞ —É–∫–∞–∑–∞–Ω–Ω—É—é –¥–∞—Ç—É"""
    print(f"–ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏–∑–∞—Ü–∏–∏ –∑–∞ {date.strftime('%d.%m.%Y')}...")
    
    # –í—ã—á–∏—Å–ª—è–µ–º –≥—Ä–∞–Ω–∏—Ü—ã –¥–Ω—è
    start_date = datetime(date.year, date.month, date.day, tzinfo=timezone.utc)
    end_date = start_date + timedelta(days=1)
    
    # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –º–æ–¥–µ–ª—å Message
    from database.models import Message
    
    # –ü–æ–ª—É—á–∞–µ–º –≤—Å–µ —Å–æ–æ–±—â–µ–Ω–∏—è –∑–∞ —É–∫–∞–∑–∞–Ω–Ω—É—é –¥–∞—Ç—É —Å –∫–∞—Ç–µ–≥–æ—Ä–∏–µ–π
    session = db_manager.Session()
    try:
        messages = session.query(Message).filter(
            Message.date >= start_date,
            Message.date < end_date,
            Message.category != None
        ).all()
        print(f"  –ù–∞–π–¥–µ–Ω–æ {len(messages)} –∫–∞—Ç–µ–≥–æ—Ä–∏–∑–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏")
    finally:
        session.close()
    
    if not messages:
        print("  –ù–µ—Ç –∫–∞—Ç–µ–≥–æ—Ä–∏–∑–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏")
        return None
    
    # –°–æ–∑–¥–∞–µ–º –∞–≥–µ–Ω—Ç–∞-–∫—Ä–∏—Ç–∏–∫–∞
    critic = CriticAgent(db_manager)
    
    results = {
        "status": "success",
        "total": len(messages),
        "updated": 0,
        "unchanged": 0,
        "errors": 0,
        "details": []
    }
    
    print(f"  –ü—Ä–æ–≤–µ—Ä–∫–∞ {len(messages)} —Å–æ–æ–±—â–µ–Ω–∏–π...")
    
    for message in messages:
        result = critic.review_categorization(message.id, message.category)
        results["details"].append(result)
        
        if result["status"] == "updated":
            results["updated"] += 1
            print(f"  –ò—Å–ø—Ä–∞–≤–ª–µ–Ω–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏—è —Å–æ–æ–±—â–µ–Ω–∏—è ID={message.id}: '{result['old_category']}' -> '{result['new_category']}'")
        elif result["status"] == "unchanged":
            results["unchanged"] += 1
        else:
            results["errors"] += 1
    
