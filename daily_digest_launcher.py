async def collect_data_for_date(client, db_manager, date):
    """–°–±–æ—Ä –¥–∞–Ω–Ω—ã—Ö –∑–∞ —É–∫–∞–∑–∞–Ω–Ω—É—é –¥–∞—Ç—É"""
    print(f"–°–±–æ—Ä –¥–∞–Ω–Ω—ã—Ö –∑–∞ {date.strftime('%d.%m.%Y')}...")
    
    # –í—ã—á–∏—Å–ª—è–µ–º –≥—Ä–∞–Ω–∏—Ü—ã –¥–Ω—è —Å —á–∞—Å–æ–≤—ã–º –ø–æ—è—Å–æ–º
    start_date = datetime(date.year, date.month, date.day, tzinfo=timezone.utc)
    end_date = start_date + timedelta(days=1)
    
    print(f"  –í—Ä–µ–º–µ–Ω–Ω–æ–π –¥–∏–∞–ø–∞–∑–æ–Ω: {start_date} - {end_date}")
    
    total_messages = 0
    
    for channel in TELEGRAM_CHANNELS:
        print(f"–û–±—Ä–∞–±–æ—Ç–∫–∞ –∫–∞–Ω–∞–ª–∞ {channel}...")
        try:
            entity = await client.get_entity(channel)
            messages = await client.get_messages(
                entity,
                limit=50,  # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º –ª–∏–º–∏—Ç, —á—Ç–æ–±—ã –æ—Ö–≤–∞—Ç–∏—Ç—å –±–æ–ª—å—à–µ —Å–æ–æ–±—â–µ–Ω–∏–π
                offset_date=end_date
            )
            
            saved_count = 0
            for msg in messages:
                # –í—ã–≤–æ–¥–∏–º –¥–∞—Ç—É —Å–æ–æ–±—â–µ–Ω–∏—è –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏
                print(f"  –°–æ–æ–±—â–µ–Ω–∏–µ #{msg.id}, –¥–∞—Ç–∞: {msg.date}")
                
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ –Ω—É–∂–Ω–æ–º –¥–∏–∞–ø–∞–∑–æ–Ω–µ –¥–∞—Ç
                if start_date <= msg.date < end_date and msg.message:
                    try:
                        db_manager.save_message(
                            channel=channel,
                            message_id=msg.id,
                            text=msg.message,
                            date=msg.date
                        )
                        saved_count += 1
                        print(f"  –°–æ—Ö—Ä–∞–Ω–µ–Ω–æ —Å–æ–æ–±—â–µ–Ω–∏–µ #{msg.id}")
                    except Exception as e:
                        print(f"  –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–∏ —Å–æ–æ–±—â–µ–Ω–∏—è: {str(e)}")
            
            print(f"  –°–æ—Ö—Ä–∞–Ω–µ–Ω–æ {saved_count} —Å–æ–æ–±—â–µ–Ω–∏–π –∏–∑ –∫–∞–Ω–∞–ª–∞ {channel}")
            total_messages += saved_count
            
        except Exception as e:
            print(f"  –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –∫–∞–Ω–∞–ª–∞ {channel}: {str(e)}")
    
    print(f"–í—Å–µ–≥–æ —Å–æ–±—Ä–∞–Ω–æ {total_messages} —Å–æ–æ–±—â–µ–Ω–∏–π –∑–∞ {date.strftime('%d.%m.%Y')}")
    return total_messages

